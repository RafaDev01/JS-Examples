//sintaxe do objeto literal, as propriedades são formadas por -> { chave: valor, };
var produto = {
  id: 9,
  nome: "Cafeteira Elétrica",
  valor: 99.00,
  disponivel: true
};

//sintaxe para imprimir o dados desejado nome-do-objeto.chave
console.log(produto.id); // 9
console.log(produto.nome); // Cafeteira Elétrica
console.log(produto.valor); // 99.00
console.log(produto.disponivel);//true